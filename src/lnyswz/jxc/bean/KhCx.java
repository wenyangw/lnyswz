package lnyswz.jxc.bean;

import java.math.BigDecimal;

public class KhCx {
	private String khbh;
	private String khmc;
	private BigDecimal sxje;
	private int sxzq;
	private BigDecimal ysje;
	private BigDecimal thje;
	
	
	public String getKhbh() {
		return khbh;
	}
	public void setKhbh(String khbh) {
		this.khbh = khbh;
	}
	public String getKhmc() {
		return khmc;
	}
	public void setKhmc(String khmc) {
		this.khmc = khmc;
	}
	public BigDecimal getSxje() {
		return sxje;
	}
	public void setSxje(BigDecimal sxje) {
		this.sxje = sxje;
	}
	public int getSxzq() {
		return sxzq;
	}
	public void setSxzq(int sxzq) {
		this.sxzq = sxzq;
	}
	public BigDecimal getYsje() {
		return ysje;
	}
	public void setYsje(BigDecimal ysje) {
		this.ysje = ysje;
	}
	public BigDecimal getThje() {
		return thje;
	}
	public void setThje(BigDecimal thje) {
		this.thje = thje;
	}
	
}
